'''
Write a python script to add multi line comments and print values of four variables,
each in a new line. Variable contains any values.
'''

"""
Python ignores the string literals that are not assigned to a variable. So, we can use these string
literals as a comment.
"""

employeeId = 1261
employeeName = "Mohsin Bashir Najar"
employeeAge = 25
employeeAddress = 'Ganderbal, Jammu & Kashmir, 191131'

print("Employee Id:", employeeId, "\nEmployee Name:", employeeName, "\nEmployee Age:", employeeAge, "\nEmployee Address:", employeeAddress, end="")

